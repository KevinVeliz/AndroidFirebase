package com.example.firebaseapp;

public class Personas {
    String name;
    String ocupacion;

    public Personas(String name, String ocupacion) {
        this.name = name;
        this.ocupacion = ocupacion;
    }



    public String getName() {
        return name;
    }

    public String getOcupacion() {
        return ocupacion;
    }



}
